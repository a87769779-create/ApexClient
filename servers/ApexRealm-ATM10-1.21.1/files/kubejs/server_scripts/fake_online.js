NetworkEvents.serverListPing(event => {
    // --- НАСТРОЙКИ ---

    // Сколько "фейковых" игроков добавлять к реальному онлайну
    // Можно поставить фиксированное число или диапазон, например: Math.floor(Math.random() * 5) + 5
    var extraPlayers = 5;

    // Список имен, которые будут показываться при наведении мышки в лаунчере
    // (Или в виджетах, если они читают список игроков)
    var fakeNames = [
        "Alex_Pro",
        "Miner2024",
        "CraftMaster",
        "BuildBuilder",
        "RedstoneKing"
    ];

    // --- ЛОГИКА ---

    // Добавляем фейковое количество к реальному
    event.players.online += extraPlayers;

    // (Опционально) Можно увеличить макс. онлайн, чтобы всем хватило места
    // event.players.max = 100;

    // Добавляем фейковые имена в список "образцов" (sample)
    // Это то, что видно, когда наводишь курсор на количество игроков в меню сетевой игры
    fakeNames.forEach(name => {
        // Создаем профиль игрока. UUID можно генерировать или оставить пустым (тогда будет рандом)
        event.players.sample.add(name);
    });

    // Описание сервера (MOTD) тоже можно менять тут
    // event.description = "§6ApexRealm §7- §aЛучший сервер!"
});
